package edu.aca.lessonoop.inheritance;

public class TruckBed {

	private int bedType;
	private int bedVolume;
	private int length;
	private boolean isExtensible;
	int getBedType() {
		return bedType;
	}
	void setBedType(int bedType) {
		this.bedType = bedType;
	}
	int getBedVolume() {
		return bedVolume;
	}
	void setBedVolume(int bedVolume) {
		this.bedVolume = bedVolume;
	}
	int getLength() {
		return length;
	}
	void setLength(int length) {
		this.length = length;
	}
	boolean isExtensible() {
		return isExtensible;
	}
	void setExtensible(boolean isExtensible) {
		this.isExtensible = isExtensible;
	}
	
	
}
